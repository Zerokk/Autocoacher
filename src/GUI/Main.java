    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
       
        if(table.getSelectedRow() != -1){
            int j = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this topic?");
            
            if(j==0){
                db.deleteTopic((int)table.getValueAt(table.getSelectedRow(), 0));
                actualizar();
            }
        }
        
    }//GEN-LAST:event_removeActionPerformed

    private void resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetActionPerformed
        
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to reset this topic?");
        if(confirm == 0){
            db.resetTopic((int)table.getValueAt(table.getSelectedRow(), 0));
            
        }
        
        
        
    }//GEN-LAST:event_resetActionPerformed

    private void exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportActionPerformed
        
        int conf = JOptionPane.showConfirmDialog(this, "Confirm if you also want to export your registries");
        if(conf != 2){
            Topic topic = db.getTopic((int)table.getValueAt(table.getSelectedRow(), 0));
            JFileChooser chooser = new JFileChooser();
            
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            chooser.showOpenDialog(new JFrame());
            String absolutePath = chooser.getSelectedFile().getAbsolutePath();
            
            db.exportTopic(topic.getId(), conf, absolutePath);
        }
        
    }//GEN-LAST:event_exportActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
          
            JFileChooser chooser = new JFileChooser();
            
            chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
            chooser.showOpenDialog(new JFrame());
            String absolutePath = chooser.getSelectedFile().getAbsolutePath();
            
            db.importTopic(new File(absolutePath));
            actualizar();
    }//GEN-LAST:event_jButton3ActionPerformed

