    public void actualizar(){
        modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"ID", "Name"});
        ResultSet topics = db.getTopics();
        try {
            
        while(topics.next()){
            
                modelo.addRow(new Object[]{topics.getInt("id"), topics.getString("name")});
            
        }
        } catch (SQLException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        table.setModel(modelo);
    }

