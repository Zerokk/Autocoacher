    private void tableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMousePressed
      
        if(table.getSelectedRow() != -1){
            reset.setEnabled(true);
            export.setEnabled(true);
            remove.setEnabled(true);
        } else {
            reset.setEnabled(false);
            export.setEnabled(false);
            remove.setEnabled(false);
        }
        
        if(table.getSelectedRow()!=clickedTab){
            clickedTab = table.getSelectedRow();
        }else{
            AdvicesManager am = new AdvicesManager();
            
            am.advices = db.getAdvicesForTopic((int)modelo.getValueAt(table.getSelectedRow(),0));
            am.multiAdvices = db.getMultiAdvicesForTopic((int)modelo.getValueAt(table.getSelectedRow(),0));
            am.topic = db.getTopic((int)modelo.getValueAt(table.getSelectedRow(),0));
            
            CoachingFrame cf = new CoachingFrame(am);
            cf.setVisible(true);
            
            
        }
        
        
        
        
    }//GEN-LAST:event_tableMousePressed

